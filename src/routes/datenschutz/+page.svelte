                Buchstaben, die auf dem von Ihnen genutzten Endgerät abgelegt
                und gespeichert werden. Cookies dienen vorrangig dazu,
                Informationen zwischen dem von Ihnen genutzten Endgerät und
                unserer Webseite auszutauschen. Hierzu gehören u. a. die
                Spracheinstellungen auf einer Webseite, der Login-Status oder
                die Stelle, an der ein Video geschaut wurde.
            </p>
            <p>
                Beim Besuch unserer Webseiten werden zwei Typen von Cookies
                eingesetzt:
            </p>
