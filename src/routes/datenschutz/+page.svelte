                    <strong>Temporäre Cookies (Session Cookies):</strong>
                    Diese speichern eine sogenannte Session-ID, mit welcher sich
                    verschiedene Anfragen Ihres Browsers der gemeinsamen Sitzung
                    zuordnen lassen. Die Session-Cookies werden gelöscht, wenn Sie
                    sich ausloggen oder Ihren Browser schließen.
                </li>
                <li>
                    <strong>Permanente Cookies:</strong>
                    Permanente Cookies bleiben auch nach dem Schließen des Browsers
                    gespeichert. Dadurch erkennt unsere Webseite Ihren Rechner wieder,
                    wenn Sie auf unsere Webseite zurückkehren. In diesen Cookies
                    werden beispielsweise Informationen zu Spracheinstellungen oder
                    Log-In-Informationen gespeichert. Außerdem kann mit diesen Cookies
                    Ihr Surfverhalten dokumentiert und gespeichert werden. Diese
                    Daten können zu Statistik-, Marketing- und Personalisierungs-Zwecken
                    verwendet werden.
                </li>
            </ul>
